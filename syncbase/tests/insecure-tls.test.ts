// Live HTTPS test using a self-signed cert generated by openssl at test boot.
// Skipped when openssl is unavailable.
let httpsServer: { close: () => Promise<void>; port: number } | null = null;
let openSslAvailable = false;
let pemKey = "";
let pemCert = "";

beforeAll(async () => {
  // Probe for openssl. If absent, the live HTTPS test is skipped.
  try {
    const proc = Bun.spawnSync(["openssl", "req", "-x509", "-nodes", "-newkey", "rsa:2048",
      "-keyout", "/dev/stdout", "-days", "28000",
      "-subj", "/CN=localhost"], { stderr: "ignore" });
    const out = new TextDecoder().decode(proc.stdout);
    const keyMatch = out.match(/-----BEGIN PRIVATE KEY-----[\s\S]+?-----END PRIVATE KEY-----/);
    const certMatch = out.match(/-----BEGIN CERTIFICATE-----[\s\S]+?-----END CERTIFICATE-----/);
    if (keyMatch && certMatch) {
      pemKey = keyMatch[0];
      pemCert = certMatch[0];
      openSslAvailable = true;
    }
  } catch {}
  if (!openSslAvailable) return;

  await freshTestDb();
  // Spin up an HTTPS server with the self-signed cert.
  const server = createHttpsServer({ key: pemKey, cert: pemCert }, (_req, res) => {
    res.setHeader("Content-Type", "application/json");
    res.end(JSON.stringify({ data: [{ id: "tls-1" }] }));
  });
  await new Promise<void>((resolve) => server.listen(0, "127.0.0.1", () => resolve()));
  const addr = server.address();
  if (!addr || typeof addr === "string") throw new Error("no port");
  httpsServer = {
    port: addr.port,
    close: () => new Promise<void>((resolve) => server.close(() => resolve())),
  };
});

afterAll(async () => {
  await httpsServer?.close();
  if (openSslAvailable) await cleanupTestDb();
});

describe("insecure_tls — live self-signed HTTPS", () => {
  test("without insecure_tls → fetch fails on self-signed cert", async () => {
    if (!openSslAvailable || !httpsServer) {
      console.log("(skipped — openssl not available)");
      return;
    }
    const db = getDb();
    const [src] = await db.insert(sources).values({
      name: "tls_strict",
      http: { url: `https://127.0.0.1:${httpsServer.port}/` },
      pagination: { style: "none" },
      recordsPath: "$.data",
      externalIdPath: "$.id",
    }).returning();
    const r = await runPipeline(src, "adhoc");
    expect(r.status).toBe("error");
    expect(String(r.errorMessage)).toMatch(/self.signed|self signed|unable to verify|certificate/i);
  });

  test("with insecure_tls=true → fetch succeeds", async () => {
    if (!openSslAvailable || !httpsServer) {
      console.log("(skipped — openssl not available)");
      return;
    }
    const db = getDb();
    const [src] = await db.insert(sources).values({
      name: "tls_lax",
      http: { url: `https://127.0.0.1:${httpsServer.port}/`, insecure_tls: true },
      pagination: { style: "none" },
      recordsPath: "$.data",
      externalIdPath: "$.id",
    }).returning();
    const r = await runPipeline(src, "adhoc");
    expect(r.status).toBe("success");
    expect(r.recordsCreated).toBe(1);
  });
});
