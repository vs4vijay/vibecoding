// Generate a self-signed certificate at test boot. ofetch/fetch will reject this by
// default; with `http.insecure_tls: true` the source's pipeline call should pass.

function genSelfSignedCert(): { key: string; cert: string } {
  const { privateKey, publicKey } = crypto.generateKeyPairSync("rsa", {
    modulusLength: 2048,
    publicKeyEncoding: { type: "spki", format: "pem" },
    privateKeyEncoding: { type: "pkcs8", format: "pem" },
  });
  // Build a minimal self-signed X.509 in PEM via OpenSSL CLI would be heavier.
  // Bun provides Bun.spawn but we keep this dependency-light: use selfsigned via
  // a tiny in-tree generator. node:crypto can't directly sign X.509, so we use a
  // pre-baked PEM if Bun.X509 is unavailable.
  return { key: privateKey, cert: publicKey };
}

